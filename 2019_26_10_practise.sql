SELECT PlanetName
	,OpeningYear
	,Opener
FROM Planets
WHERE PlanetName NOT LIKE 'S%'
	AND PlanetName NOT LIKE '%s'

/*4. –ò—Å–ø–æ–ª—å–∑—É—è –æ–ø–µ—Ä–∞—Ç–æ—Ä—ã SQL WHERE  –≤—ã–≤–µ—Å—Ç–∏ –∑–∞–ø–∏—Å–∏ –ø–ª–∞–Ω–µ—Ç, —É –∫–æ—Ç–æ—Ä—ã—Ö —Ä–∞–¥–∏—É—Å –ø–ª–∞–Ω–µ—Ç—ã –º–µ–Ω—å—à–µ 10000 –∏ –æ—Ç–∫—Ä—ã—Ç—ã—Ö (OpeningYear) –ø–æ—Å–ª–µ 1620.*/
SELECT *
FROM Planets
WHERE Radius < 10000
	AND OpeningYear > 1620

/*5. –ò—Å–ø–æ–ª—å–∑—É—è –æ–ø–µ—Ä–∞—Ç–æ—Ä—ã SQL WHERE –≤—ã–≤–µ—Å—Ç–∏ –∑–∞–ø–∏—Å–∏ –ø–ª–∞–Ω–µ—Ç, –Ω–∞–∑–≤–∞–Ω–∏—è –∫–æ—Ç–æ—Ä—ã—Ö –Ω–∞—á–∏–Ω–∞—é—Ç—Å—è —Å –±—É–∫–≤—ã ¬´N¬ª –∏–ª–∏ –∑–∞–∫–∞–Ω—á–∏–≤–∞—é—Ç—Å—è –Ω–∞ –±—É–∫–≤—É ¬´s¬ª –∏ –Ω–µ –∏–º–µ—é—â–∏–µ –∫–æ–ª–µ—Ü.*/
SELECT *
FROM Planets
WHERE (
		PlanetName LIKE 'N%'
		OR PlanetName LIKE '%s'
		)
